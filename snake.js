//Scores
var currentScore = 0;
var endScore = 0;
var scoreDisplay = document.getElementById("currentScore");
var highScore = 0;
var highScoreDisplay = document.getElementById("highScore");

//board
var blockSize = 25;
var rows = 20;
var cols = 20;
var board;
var context; 

//snake head
var snakeX = blockSize * 5;
var snakeY = blockSize * 5;

var velocityX = 0;
var velocityY = 0;

var snakeBody = [];

//food
var foodX;
var foodY;

var gameOver = false;

window.onload = function() {
    board = document.getElementById("board");
    board.height = rows * blockSize;
    board.width = cols * blockSize;
    context = board.getContext("2d"); //used for drawing on the board

    placeFood();
    document.addEventListener("keyup", changeDirection);
    // update();
    setInterval(update, 1000/10); //100 milliseconds
}

function update() {
    if (gameOver) {
        return;
    }

    context.fillStyle="black";
    context.fillRect(0, 0, board.width, board.height);

    context.fillStyle="red";
    context.fillRect(foodX, foodY, blockSize, blockSize);

    if (snakeX == foodX && snakeY == foodY) {
        snakeBody.push([foodX, foodY]);
        placeFood();
        currentScore++
        updateScore();
    }

    for (let i = snakeBody.length-1; i > 0; i--) {
        snakeBody[i] = snakeBody[i-1];
    }
    if (snakeBody.length) {
        snakeBody[0] = [snakeX, snakeY];
    }

    context.fillStyle="lime";
    snakeX += velocityX * blockSize;
    snakeY += velocityY * blockSize;
    context.fillRect(snakeX, snakeY, blockSize, blockSize);
    for (let i = 0; i < snakeBody.length; i++) {
        context.fillRect(snakeBody[i][0], snakeBody[i][1], blockSize, blockSize);
    }

    //game over conditions
    if (snakeX < 0 || snakeX > cols*blockSize || snakeY < 0 || snakeY > rows*blockSize) {
        gameOver = true;
        updateScore();
    }

    for (let i = 0; i < snakeBody.length; i++) {
        if (snakeX == snakeBody[i][0] && snakeY == snakeBody[i][1]) {
            gameOver = true;
            updateScore();
        }
    }
}

function changeDirection(e) {
    if (e.code == "ArrowUp" && velocityY != 1) {
        velocityX = 0;
        velocityY = -1;
    }
    else if (e.code == "ArrowDown" && velocityY != -1) {
        velocityX = 0;
        velocityY = 1;
    }
    else if (e.code == "ArrowLeft" && velocityX != 1) {
        velocityX = -1;
        velocityY = 0;
    }
    else if (e.code == "ArrowRight" && velocityX != -1) {
        velocityX = 1;
        velocityY = 0;
    }
}


function placeFood() {
    //(0-1) * cols -> (0-19.9999) -> (0-19) * 25
    foodX = Math.floor(Math.random() * cols) * blockSize;
    foodY = Math.floor(Math.random() * rows) * blockSize;
}

function updateScore(){
    if(gameOver == false){
        scoreDisplay.innerText = "Current Score: "+currentScore;
    }
    if (gameOver == true){
        if(highScore < currentScore){
            highScore = currentScore;
            var newHighScore = confirm("Congradulations, you attained a new high score of "+highScore+". Would you like to save your score?");
            if(newHighScore == true){
                localStorage.setItem(highScore);
                highScoreDisplay.innerText = "High Score: "+highScore;
                playAgain();
            }
            else{
                playAgain();
            }
        }
        else{
            currentScore = currentScore;
            playAgain();
        }
    }
    
}

function playAgain(){
    var playAgain = prompt("Your Current Score is "+currentScore+". Would you like to play again? Type y for Yes and n for no");
    if(playAgain == "y"){
        location.reload();
    }
    else if(playAgain == "n"){
        alert("Thank You for playing. For more, see https://github.com/AidenSPS for more games and applications");
        window.close();
    }
    else{
        alert("Please choose y or n to confirm");
    }
}